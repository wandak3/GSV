def scrape_other_languages(url, name, value):
    response = requests.get(url)
    soup = BeautifulSoup(response.text, 'html.parser')
    spans = soup.find_all('span', {'lang': 'vi'})
    if len(spans) != 0:
        if spans[0].string == None:
          print({ "name": "{} ({})".format(name, spans[0].text) , "value": value }, ",")
        else:
          print({ "name": "{} ({})".format(name, spans[0].string) , "value": value }, ",")
    

